    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
        String clientDni = dni.getText();
        String clientName = name.getText();
        String clientLastName = lastName.getText();
        int clientNumber = Integer.parseInt(number.getText());
        String clientStreet = street.getText();
        String clientCountry = country.getText();
        String clientProvince = province.getText();
        int vpId = Integer.parseInt(vacationalPackageId.getText());
        
        int i = 0;
        String[] clientPhone = new String[4]; 
        clientPhone[i++] = phone1.getText();
        clientPhone[i++] = phone2.getText();
        clientPhone[i++] = phone3.getText();
        clientPhone[i++] = phone4.getText();
        
        controller.create(clientDni, clientName, clientLastName, clientPhone, 
            clientNumber, clientStreet, clientCountry, clientProvince, vpId);
        
        
    }//GEN-LAST:event_insertActionPerformed


