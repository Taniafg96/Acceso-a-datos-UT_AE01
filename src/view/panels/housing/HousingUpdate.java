    public void showTable(){
        DefaultTableModel modelTable = (DefaultTableModel) housingTable.getModel();
        
        modelTable.setRowCount(0);
        
        for(Object[] i : controller.read()){
           modelTable.addRow(i);
        }
        
        housingTable.setModel(modelTable);
    }
    
    
