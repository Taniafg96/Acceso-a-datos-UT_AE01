    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String housingHouse = house.getText();
        String houseType = housingType.getSelectedItem().toString();
        int housingCategory = Integer.parseInt(category.getSelectedItem().toString());
        
        if(id.getText().matches("^[0-9]")){
            int housingId = Integer.parseInt(id.getText());
            int result = JOptionPane.showConfirmDialog(null, "¿Está seguro que quiere actualizar el campo?", "", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
             
            if(result == 0) controller.update(housingId, housingHouse, houseType, housingCategory);
        }
        else JOptionPane.showMessageDialog(null, "El id no puede tiene que ser un número");
        
        showTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void housingTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_housingTableMouseClicked
          int row = housingTable.getSelectedRow();
          
          id.setText(String.valueOf(housingTable.getValueAt(row, 0)));
          house.setText(String.valueOf(housingTable.getValueAt(row, 1)));
          String house = String.valueOf(housingTable.getValueAt(row, 3));
          switch(house){
              case "Hotel": housingType.setSelectedIndex(0);
              break;
              case "Hostal": housingType.setSelectedIndex(1);
              break;
              case "Apartamento": housingType.setSelectedIndex(2);
              break;
          }

        category.setSelectedIndex(Integer.parseInt(String.valueOf(housingTable.getValueAt(row, 4))) - 1);
          
    }//GEN-LAST:event_housingTableMouseClicked

